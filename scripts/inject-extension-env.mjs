/**
 * Reads repo-root .env and writes extension/aura-config.js for the Chrome extension.
 * Run: npm run extension:env (also runs on npm install via postinstall).
 */
import fs from 'fs';
import { dirname, join } from 'path';
import { fileURLToPath } from 'url';
import dotenv from 'dotenv';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');
const outPath = join(root, 'extension', 'aura-config.js');

dotenv.config({ path: join(root, '.env') });

/** .env values sometimes include wrapping quotes or spaces */
function clean(v) {
  if (v == null) return '';
  let s = String(v).trim();
  if ((s.startsWith("'") && s.endsWith("'")) || (s.startsWith('"') && s.endsWith('"'))) {
    s = s.slice(1, -1).trim();
  }
  return s;
}

const gemini = clean(process.env.GEMINI_API_KEY || process.env.GEM_KEY);
const eleven = clean(process.env.ELEVENLABS_API_KEY || process.env.API_KEY);
const voice = clean(process.env.ELEVENLABS_VOICE_ID) || 'kE6lVLC9rXp4T2rZ8dMw';

const banner = `/* Generated by npm run extension:env — do not edit by hand. */\n`;

const body = `${banner}const AURA_GEMINI_API_KEY = ${JSON.stringify(gemini)};
const AURA_ELEVENLABS_API_KEY = ${JSON.stringify(eleven)};
const AURA_ELEVENLABS_VOICE_ID = ${JSON.stringify(voice)};
`;

fs.mkdirSync(dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, body, 'utf8');

if (!gemini && !eleven) {
  console.warn(
    '[extension:env] No GEMINI_API_KEY / ELEVENLABS_API_KEY in .env — wrote empty aura-config.js. Copy .env.example → .env and fill keys.'
  );
} else {
  console.log('[extension:env] Wrote extension/aura-config.js');
}
